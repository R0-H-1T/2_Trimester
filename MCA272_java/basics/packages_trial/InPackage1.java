package package1;

public class InPackage1 {
    public int age1 = 23;
    protected int age2 = 45;
    private int age3 = 90;
    int age4 = 82;

    public InPackage1 () {
        System.out.println("Age1: " + age1);
        System.out.println("Age2: " + age2);
        System.out.println("Age3: " + age3);
        System.out.println("Age4: " + age4);
    }
}